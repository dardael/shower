import 'reflect-metadata';
import React from 'react';
import type { Metadata } from 'next';
import './globals.css';
import {
  initializeDatabaseForLayout,
  fetchWebsiteName,
  fetchWebsiteIcon,
} from '@/infrastructure/shared/layoutUtils';
import { Provider } from '@/presentation/shared/components/ui/provider';
import { SocialNetworksFooterContainer } from '@/presentation/shared/components/SocialNetworksFooter';
import { PublicHeaderMenuContainer } from '@/presentation/shared/components/PublicHeaderMenu';

// Force dynamic rendering to prevent static generation during build
export const dynamic = 'force-dynamic';
export const runtime = 'nodejs';

async function getWebsiteName(): Promise<string> {
  return fetchWebsiteName();
}

async function getWebsiteIcon(): Promise<string | null> {
  return fetchWebsiteIcon();
}

export async function generateMetadata(): Promise<Metadata> {
  const websiteName = await getWebsiteName();
  const websiteIcon = await getWebsiteIcon();

  return {
    title: websiteName,
    description: 'Generated by create next app',
    icons: websiteIcon
      ? {
          icon: websiteIcon,
          shortcut: websiteIcon,
          apple: websiteIcon,
        }
      : undefined,
  };
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  // Initialize database connection for public layout
  await initializeDatabaseForLayout();

  return (
    <html suppressHydrationWarning>
      <body>
        <Provider>
          <PublicHeaderMenuContainer />
          {children}
          <SocialNetworksFooterContainer />
        </Provider>
      </body>
    </html>
  );
}
